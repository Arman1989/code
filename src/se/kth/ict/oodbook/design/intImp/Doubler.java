package se.kth.ict.oodbook.design.intImp;

public class Doubler {

    /**
     * Doubles the operand and returns the result.
     */
    public int multiplyWithTwo(int operand) {
        return operand * 2;
    }

    /**
     * Doubles the operand and returns the result.
     */
    public int multiplyWithTwo(int operand) {
        return operand << 1;
    }

}
